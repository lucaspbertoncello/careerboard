// Fun√ß√£o para gerar data aleat√≥ria
function randomDate(start: Date, end: Date): Date {
  return new Date(start.getTime() + Math.random() * (end.getTime() - start.getTime()));
}

// Fun√ß√£o para gerar status aleat√≥rio
function randomStatus(): InterviewStatus {
  const statuses = [InterviewStatus.APPROVED, InterviewStatus.PENDING, InterviewStatus.REJECTED];
  return statuses[Math.floor(Math.random() * statuses.length)];
}

// Fun√ß√£o para gerar sal√°rio aleat√≥rio
function randomSalary(): number {
  return Math.floor(Math.random() * (20000 - 5000 + 1) + 5000);
}

async function main() {
  console.log("üå± Starting seed...");

  // Definir per√≠odos para distribuir as entrevistas
  const periods = [
    { start: new Date("2020-01-01"), end: new Date("2020-12-31"), count: 30 }, // 30 em 2020
    { start: new Date("2021-01-01"), end: new Date("2021-12-31"), count: 40 }, // 40 em 2021
    { start: new Date("2022-01-01"), end: new Date("2022-12-31"), count: 50 }, // 50 em 2022
    { start: new Date("2023-01-01"), end: new Date("2023-12-31"), count: 60 }, // 60 em 2023
    { start: new Date("2024-01-01"), end: new Date("2024-12-31"), count: 100 }, // 100 em 2024
    { start: new Date("2025-01-01"), end: new Date("2025-10-14"), count: 220 }, // 220 em 2025 (mais dados para testar)
  ];

  let totalCreated = 0;

  for (const period of periods) {
    console.log(`üìÖ Creating ${period.count} interviews for period ${period.start.getFullYear()}...`);

    const interviews: Array<{
      userId: string;
      companyName: string;
      role: string;
      appliedAt: Date;
      salary: number;
      status: InterviewStatus;
      description: string;
    }> = [];

    for (let i = 0; i < period.count; i++) {
      const company = companies[Math.floor(Math.random() * companies.length)];
      const role = roles[Math.floor(Math.random() * roles.length)];
      const description = descriptions[Math.floor(Math.random() * descriptions.length)];
      const appliedAt = randomDate(period.start, period.end);
      const salary = randomSalary();
      const status = randomStatus();

      interviews.push({
        userId: USER_ID,
        companyName: company,
        role,
        appliedAt,
        salary,
        status,
        description,
      });
    }

    // Criar em lote
    await prisma.interview.createMany({
      data: interviews,
      skipDuplicates: true,
    });

    totalCreated += period.count;
    console.log(`‚úÖ Created ${period.count} interviews for ${period.start.getFullYear()}`);
  }

  console.log(`üéâ Seed completed! Total interviews created: ${totalCreated}`);
}

main()
  .catch((e) => {
    console.error("‚ùå Error seeding database:", e);
    process.exit(1);
  })
  .finally(() => {
    void prisma.$disconnect();
  });
